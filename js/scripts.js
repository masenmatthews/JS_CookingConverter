var galToLiter = function(gallons) {
  var liters = gallons * 3.79;
  return liters;
};

var ozToTbsp = function(oz) {
  var tbsp = oz * 2;
  return tbsp;
};

var ozToCup = function(oz) {
  var cup = oz / 8;
  return cup;
};

var ozToTsp = function(oz) {
  var tsp = oz * 6;
  return tsp;
};

var ozToQuart = function(oz) {
  var quart = oz / 32;
  return quart;
};
